t1=(3,6,9,12,15,18,21)
print(t1[4])
#15
print(t1[-3])
#15

print(t1[::])
#(3, 6, 9, 12, 15, 18, 21)
print(t1[2:])
#(9, 12, 15, 18, 21)

print(t1[2:5])
#(9, 12, 15)
print(t1[-5:-2])
#(9, 12, 15)


print(t1[::2])
#(3, 9, 15, 21)

print(t1[::-1])
#
print(t1[4::-1])
#(15, 12, 9, 6, 3)

print(t1[4:0:-1])
#(15, 12, 9, 6)
print(t1[-3:-7:-1])
#(15, 12, 9, 6)


