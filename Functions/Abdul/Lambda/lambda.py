L1=[[4,2,"Six"],[1,4,"Five"],[2,2,"Four"]]

print(sorted(L1))
# [[1, 4, 'Five'], [2, 2, 'Four'], [4, 2, 'Six']]

L2=sorted(L1,key=lambda x: x[0]+x[1])
