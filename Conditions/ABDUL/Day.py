############## Calender Month #####################
Month=eval(input("Enter the month number (1-12): "))    

if Month == 1:
    print("January")
elif Month == 2:
    print("February")
elif Month == 3:  
    print("March")
else:
    print("Invalid month number. Please enter a number between 1 and 12.")

######################### Digit in words #####################
digit=eval(input("Enter the digit (0-9): "))

if digit == 0:
    print("Zero")
elif digit == 1:
    print("One")
elif digit == 2:
    print("Two")
elif digit == 3:
    print("Three")
elif digit == 4:
    print("Four")
elif digit == 5:
    print("Five")
elif digit == 6:
    print("Six")
elif digit == 7:
    print("Seven")
elif digit == 8:
    print("Eight")
elif digit == 9:
    print("Nine")
else:
    print("Invalid digit. Please enter a digit between 0 and 9.")




